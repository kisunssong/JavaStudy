package day03_test;

public class BitShiftExample {
		
		
	public static void main (String[] args) {
		
		
		
		int a = 192;
		System.out.println("a(192)\t"  + a);
		
		int a1 = a << 3;
		System.out.println("a<<3\t" + a1);
		
		int a2 = a >> 3;
		System.out.println("a>>3\t" + a2);
		
		int a3 = -192;
		System.out.println("a(-192)\t" + a);
		
		
		
	}

}
