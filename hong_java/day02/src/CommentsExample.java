public class CommentsExample {
    
	/*
	main함수(메서드)는 프로그램을 실행하기 위해 반드시
	작성해야 하는 함수(메서드)입니다.
	
	main함수 블록({})안에 들어있는 코드의 명령들을 실행합니다.
	
	main함수를 쉽게 작성하시려면 이클릭스 코드작성부에
	'main'이라고 입력하고 Ctrl + Space를 누르면
	자동완성기능으로 작성할 수 있습니다.
	*/
    public static void main(String[] args) {
		
    	//코드 실행 단축키 Ctrl + F11
    	System.out.print("안녕~~!\n");
    	System.out.print("잘가~~!\n");
    	
    	/*
    	 * println()은 print()와 달리 출력 이후에
    	 * 자동으로 줄 개행을 합니다.(\n을 자동포함)
    	 */
        //코드 앞에 주석을 포함하면 코드가 실행되지 않는다.Ctrl + /
    	//System.out.println("메롱~~!");
    	System.out.println("빠이~~!");
    	
    	//sysout이라고 적고 Ctrl + Space를 누르면
    	//println이 자동 작성됩니다.
    	System.out.println("방가방가~~!!");
    	//printf를 사용해도 된다.
    	System.out.printf("%d + %d = %d\n", 10, 3, 13);
	}
}
